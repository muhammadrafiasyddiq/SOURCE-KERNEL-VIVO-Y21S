static void lcm_init_power(void)
{
	LCM_INFO("panel_reset_state = %d, lcm_software_id = 0x%x\n", panel_reset_state, lcm_software_id);
	if (panel_reset_state == 0) {
		lcm_enp_setting(1);
		MDELAY(5);
		lcm_enn_setting(1);
		MDELAY(5);
		lcm_bias_set_avdd_n_avee(55);
		MDELAY(3);
		panel_reset_state = 1;
	}
}

static void lcm_suspend_power(void)
{
	if (phone_shutdown_state) {
		LCM_INFO("[LCM]lcm_suspend_power reset keep low level\n");
		MDELAY(36);
		lcm_reset_setting(0);
	}
	MDELAY(20);
	lcm_enn_setting(0);
	MDELAY(5);
	lcm_enp_setting(0);
	MDELAY(5);
	LCM_INFO("[LCM]lcm_suspend_power ft8720- vddi keep high level\n");
	panel_reset_state = 0;
	LCM_INFO("\n");
}

static void lcm_resume_power(void)
{
	lcm_init_power();
}

static void lcm_init(void)
{
	panel_off_state = 0;

	lcm_reset_setting(0);
	MDELAY(10);
	lcm_reset_setting(1);
	MDELAY(10);
	lcm_reset_setting(0);
	MDELAY(10);
	lcm_reset_setting(1);
	MDELAY(27);
	push_table(NULL, init_setting, sizeof(init_setting) / sizeof(struct LCD_setting_table), 1);
	LCM_INFO("----pd2147 lcm_init----lcm mode = cmd mode :%d----\n", lcm_dsi_mode);
}

static void lcm_suspend(void)
{
	LCM_INFO("lcm_software_id = 0x%x, islcmconnected = %d  \n", lcm_software_id,islcmconnected);
	lcm_bkg_setting(0);
	MDELAY(5);
	push_table(NULL, lcm_suspend_setting, sizeof(lcm_suspend_setting) / sizeof(struct LCD_setting_table), 1);
	panel_off_state = 1;
	if(islcmconnected == 0){
		lcm_enn_setting(0);
		MDELAY(5);
		lcm_enp_setting(0);
	}
}

static void lcm_resume(void)
{
	lcm_bkg_setting(1);
	MDELAY(5);
	lcm_init();
}


static void lcm_update(unsigned int x, unsigned int y, unsigned int width, unsigned int height)
{
	unsigned int x0 = x;
	unsigned int y0 = y;
	unsigned int x1 = x0 + width - 1;
	unsigned int y1 = y0 + height - 1;

	unsigned char x0_MSB = ((x0 >> 8) & 0xFF);
	unsigned char x0_LSB = (x0 & 0xFF);
	unsigned char x1_MSB = ((x1 >> 8) & 0xFF);
	unsigned char x1_LSB = (x1 & 0xFF);
	unsigned char y0_MSB = ((y0 >> 8) & 0xFF);
	unsigned char y0_LSB = (y0 & 0xFF);
	unsigned char y1_MSB = ((y1 >> 8) & 0xFF);
	unsigned char y1_LSB = (y1 & 0xFF);

	unsigned int data_array[16];

	data_array[0] = 0x00053902;
	data_array[1] = (x1_MSB << 24) | (x0_LSB << 16) | (x0_MSB << 8) | 0x2a;
	data_array[2] = (x1_LSB);
	dsi_set_cmdq(data_array, 3, 1);

	data_array[0] = 0x00053902;
	data_array[1] = (y1_MSB << 24) | (y0_LSB << 16) | (y0_MSB << 8) | 0x2b;
	data_array[2] = (y1_LSB);
	dsi_set_cmdq(data_array, 3, 1);

	data_array[0] = 0x002c3909;
	dsi_set_cmdq(data_array, 1, 0);
}

static void lcm_setbacklight_cmdq(void *handle, unsigned int level)
{
	static unsigned int last_brightness_level = 1600;
	LCM_INFO("LastBacklight = %d, level=%d\n", last_brightness_level, level);
	
	if (level == LED_FULL)
		level = 4095;
	else
		level = level * 2;
	
	if (level && !last_brightness_level) {
		LCD_INFO("set all pixel on\n");
		push_table(NULL, lcm_all_pixel_on_setting, sizeof(lcm_all_pixel_on_setting) / sizeof(struct LCD_setting_table), 1);
	} else if (!level && last_brightness_level){
		LCD_INFO("set all pixel off\n");
		dimming_enable = 0;
		push_table(NULL, lcm_all_pixel_off_setting, sizeof(lcm_all_pixel_off_setting) / sizeof(struct LCD_setting_table), 1);
		MDELAY(40);
	}
	
	if ((level != 0) && (dimming_enable == 0) && (last_brightness_level != 0)) {
		push_table(handle, lcm_cmd_backlight_dimming_enable, sizeof(lcm_cmd_backlight_dimming_enable) / sizeof(struct LCD_setting_table), 1);
		dimming_enable = 1;
		MDELAY(20);	
		LCM_INFO("LastBacklight = %d,level = %d,dimming_enable = %d,\n", last_brightness_level, level, dimming_enable);
	}

	last_brightness_level = level;
	bl_lvl = level;
	bl_brightness_hal = level;

	bl_level_12bit[0].para_list[0] = (unsigned char)((level>>4)&0xFF);
	bl_level_12bit[0].para_list[1] = (unsigned char)(level&0x0F);
	
	LCM_INFO("LastBacklight = %d, level=%d, high_bit=0x%x, low_bit=0x%x\n", bl_brightness_hal, level, bl_level_12bit[0].para_list[0], bl_level_12bit[0].para_list[1]);
	push_table(handle, bl_level_12bit, sizeof(bl_level_12bit) / sizeof(struct LCD_setting_table), 1);
}
